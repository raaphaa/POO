package br.com.serratec.oficina;

public class Proprietario {
	protected String nome;

	public Proprietario(String nome) {
		super();
		this.nome = nome;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	@Override
	public String toString() {
		return nome;
	}
}