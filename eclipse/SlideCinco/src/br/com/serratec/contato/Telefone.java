package br.com.serratec.contato;

public class Telefone {
	private String numero;
	
	public Telefone() {
		super();
	}

	public Telefone(String numero) {
		super();
		this.numero = numero;
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}
}